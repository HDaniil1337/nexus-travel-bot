def generate_post(location, topic_name, emoji, topic_tag):
    url = "https://api.groq.com/openai/v1/chat/completions"
    headers = {
        "Authorization": f"Bearer {GROQ_API_KEY}",
        "Content-Type": "application/json"
    }

    loc_clean = location.split('(')[0].strip()
    loc_tag = f"#{loc_clean.replace(' ', '')}"

    prompt = f"""Ты — топовый тревел-блогер и автор Telegram-канала "Nex Travel".
Напиши экспертный пост про направление: {location}. Тема: {emoji} {topic_name}.

Язык: русский. Стиль: цепляющий, экспертный, без воды. Короткие абзацы. Будь креативным, если тема кажется необычной для этой локации.

СТРУКТУРА ПОСТА:
1. Заголовок с эмодзи (цепляющий).
2. Художественное описание локации по теме (3-4 абзаца). ЖИРНЫМ шрифтом (<b>...</b>) выделяй названия мест, отелей, пляжей или заведений.
3. Разделительная линия.
4. Раздел "📝 Краткий гайд:":
   - 💰 <b>Бюджет:</b> [1-5 эмодзи 💰] + уровень цен.
   - 📅 <b>Идеальный сезон:</b> [месяцы].
   - ✈️ <b>Где искать выгоду:</b> [ВНИМАНИЕ! Проанализируй регион {location}. Если это Азия — советуй Agoda и 12Go. Если Европа — Skyscanner и Omio. Если США/Северная Америка — Vrbo и Kayak. Если долгая зимовка/номады — Airbnb. Если СНГ — Aviasales. Напиши 2 самых подходящих сервиса под этот регион и оберни их названия в HTML-ссылки, например: <a href="https://www.agoda.com">Agoda</a>].
   - 💡 <b>Лайфхак:</b> [неочевидный совет по экономии/логистике/безопасности для {location} в рамках темы {topic_tag}].
5. Интригующий вопрос для комментариев.
6. В конце: #NexТravel {loc_tag} {topic_tag}

КРИТИЧЕСКИ ВАЖНЫЕ ТРЕБОВАНИЯ К ФОРМАТУ:
- КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО использовать звездочки (**) для выделения текста. 
- Используй ТОЛЬКО HTML-теги: <b>текст</b> и <a href="...">ссылка</a>.
- Объем: Строго 120-150 слов (не более 900 символов с пробелами). Это критически важно, так как текст будет подписью к картинке!"""

    payload = {
        "model": "llama-3.3-70b-versatile",
        "messages": [{"role": "user", "content": prompt}],
        "temperature": 0.72
    }

    response = requests.post(url, headers=headers, json=payload, timeout=30)
    data = response.json()

    try:
        content = data["choices"][0]["message"]["content"].strip()
        # 👇 Вот эта самая защита от звездочек 👇
        content = content.replace("**", "")
        return content, loc_tag
    except (KeyError, IndexError):
        print(f"❌ Ошибка Groq: {data}")
        return None, None


def send_to_telegram(text, loc_tag, topic_tag, emoji, location):
    url = f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/sendPhoto" 
    
    keyboard = {
        "inline_keyboard": [
            [
                {"text": f"🌍 Посты: {loc_tag}", "url": f"https://t.me/{CHANNEL_USERNAME}?q={urllib.parse.quote(loc_tag)}"},
                {"text": f"{emoji} Тема: {topic_tag}", "url": f"https://t.me/{CHANNEL_USERNAME}?q={urllib.parse.quote(topic_tag)}"}
            ]
        ]
    }

    # 🔥 МАГИЯ ГЕНЕРАЦИИ ФОТО 🔥
    HF_API_KEY = os.getenv("HF_API_KEY")
    image_bytes = None
    
    if HF_API_KEY:
        # Модель, которую мы используем (Stable Diffusion XL - крутой бесплатный движок)
        HF_API_URL = "https://api-inference.huggingface.co/models/stabilityai/stable-diffusion-xl-base-1.0"
        headers = {"Authorization": f"Bearer {HF_API_KEY}"}
        
        # Промпт для картинки (генерируем на английском, так качественнее)
        # Очищаем локацию и создаем запрос
        query = location.split('(')[0].strip()
        image_prompt = f"Beautiful professional travel photography of {query}, stunning landscape, photorealistic, 8k, detailed, cinematic lighting, national geographic style"
        
        try:
            # Запрашиваем генерацию
            res = requests.post(HF_API_URL, headers=headers, json={"inputs": image_prompt}, timeout=30)
            if res.status_code == 200:
                image_bytes = res.content
        except Exception as e:
            print(f"❌ Ошибка Hugging Face: {e}")

    # Если фото успешно сгенерировалось — отправляем фото с подписью
    if image_bytes:
        # Telegram принимает картинки только в виде файлов
        files = {'photo': ('photo.jpg', io.BytesIO(image_bytes), 'image/jpeg')}
        payload = {
            "chat_id": CHANNEL_ID,
            "caption": text,             # Текст поста - подпись к фото
            "parse_mode": "HTML",
            "reply_markup": keyboard
        }
        response = requests.post(url, files=files, data=payload, timeout=20)
        result = response.json()

        if result.get("ok"):
            print(f"✅ Успешно опубликовано СГЕНЕРИРОВАННОЕ фото с текстом!")
            return True
        else:
            print(f"⚠️ Ошибка Telegram при отправке фото (скорее всего текст слишком длинный): {result.get('description')}")

    # Если генерация не удалась (бывает, если API перегружено) — отправляем просто текст
    print("🔄 Генерация не удалась. Отправляю пост в текстовом формате...")
    fallback_url = f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/sendMessage"
    fallback_payload = {
        "chat_id": CHANNEL_ID,
        "text": text,
        "parse_mode": "HTML",
        "disable_web_page_preview": True,
        "reply_markup": keyboard
    }
    requests.post(fallback_url, json=fallback_payload, timeout=15)
    return False


def main():
    topic_name, emoji, topic_tag = get_topic_for_now()
    
    if topic_tag in STRICT_LOCATIONS:
        location = random.choice(STRICT_LOCATIONS[topic_tag])
    else:
        location = random.choice(ALL_LOCATIONS)

    print(f"🌍 Выбранная локация: {location}")
    print(f"📌 Тема: {emoji} {topic_name} ({topic_tag})")
    print("⏳ Генерирую премиум-пост через Groq (Llama 3.3)...")
    
    post_text, loc_tag = generate_post(location, topic_name, emoji, topic_tag)
    if not post_text or not loc_tag:
        return

    print("\n--- ТЕКСТ ПОСТА ---")
    # Убираем капс, если он вдруг остался в промпте (мы его убирали ранее)
    print(post_text)
    print("-------------------\n")

    # Передаем саму локацию (location), чтобы скрипт сгенерировал для нее фотку
    send_to_telegram(post_text, loc_tag, topic_tag, emoji, location)


if __name__ == "__main__":
    main()
